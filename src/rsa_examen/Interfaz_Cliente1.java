//----------------------------------BOTON PARA DESCIFRAR    
    private void btn_descifrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_descifrarActionPerformed
         //Descifrar
            String letra="";

            StringTokenizer st=new StringTokenizer(mensaje.getText());
            BigInteger[] textoCifrado = new BigInteger[st.countTokens()];

            for(int i=0;i<textoCifrado.length;i++){
                letra = st.nextToken();
                textoCifrado[i]=new BigInteger(letra);
            }
            mensaje_cifrado_des.setText(cifradoRSA.desencripta(textoCifrado));
    }//GEN-LAST:event_btn_descifrarActionPerformed

    //------------------------------Boton para CIFRAR
    private void btn_cifrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cifrarActionPerformed
        //Creamos la variable para encriptar el texto que se ingrese en el textarea
        BigInteger[] txtCifrado = cifradoRSA.encriptar(mensaje.getText());
        mensaje_cifrado_des.setText("");
        
        //Recorremos todo el mensaje
        for(int i=0; i<txtCifrado.length; i++) {
            
            mensaje_cifrado_des.setText(mensaje_cifrado_des.getText()+txtCifrado[i].toString()+"\t");
            
            //Ciframos el texto
            if(i != txtCifrado.length-1){
                mensaje_cifrado_des.setText(mensaje_cifrado_des.getText()+"");
                
            }
        }
    }//GEN-LAST:event_btn_cifrarActionPerformed

