//--------------------Evento para el boton de calcular n, phi, e, d-------------------------
    private void calcular_numerosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcular_numerosActionPerformed
        
        //Verificamos que los campos esten llenos
        if(!numero_p.getText().isEmpty()||!numero_q.getText().isEmpty()){
            if(!numero_p.getText().isEmpty()){
                if(!numero_q.getText().isEmpty()){
                    //Obtenemos los valores de p y q
                    BigInteger p=new BigInteger(numero_p.getText());
                    BigInteger q=new  BigInteger(numero_q.getText());
                    
                    //Claculamos los valores de d y e
                    cifradoRSA=new RSA(p, q,10);
                    clave_d.setText(""+cifradoRSA.dameD());
                    clave_e.setText(""+cifradoRSA.dameE());
                    modulo_n.setText(""+cifradoRSA.dameN());
                    phi.setText(""+cifradoRSA.damePhi());
                    
                    //Habilitamos el boton para cifrar
                    btn_cifrar.setEnabled(true);
                    btn_descifrar.setEnabled(true);
                    btn_enviar_datos.setEnabled(true);
                
                }else{
                    JOptionPane.showMessageDialog(this, "Ingrese el valor de q");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Ingrese el valor de p");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Ingrese el valor de p y q");
        }
    }//GEN-LAST:event_calcular_numerosActionPerformed

